class Solution(object):
    def strStr(self, haystack, needle):
        """
        :type haystack: str
        :type needle: str
        :rtype: int
        index:0  -1  first occurance of string
        ana 3ayz lw p2==len(needle) return p1-len(needle) 
        """
        return haystack.finc(needle)
    
         